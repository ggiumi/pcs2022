protein_seq = []
for i in range(0, len(seq), 3):
    alphabet_letter =  table[seq[i:i+3]]
    if i == alphabet_letter:
        break
    else:
        protein_seq.append(alphabet_letter)
protein_seq.remove("Stop")
print(''.join(protein_seq))